import UIKit
import SwiftUI

public enum WKColor {

	static let white = UIColor.white
	static let black = UIColor.black
	static let red = UIColor.red
	static let blue = UIColor.blue
	static let orange = UIColor.orange
	static let purple = UIColor.purple
	static let gray = UIColor.gray
	static let lightGray = UIColor.lightGray.withAlphaComponent(0.2)
	static let darkGray = UIColor.darkGray.withAlphaComponent(0.2)
	static let green = UIColor.green
	static let systemGreen = UIColor.systemGreen
	static let teal = UIColor.systemTeal
	static let cyan = UIColor.cyan

}

